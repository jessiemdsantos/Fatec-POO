    private void btnInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserirActionPerformed
        
        // Turma selecionada
        Turma turma = cadTurma.get(cboTurma.getSelectedIndex());
        
        // Obtendo objeto Aluno
        Aluno aluno = null;
        
        for (Pessoa pessoa : cadAlunoInstrutor) {
            if (
                pessoa instanceof Aluno &&
                ((Aluno) pessoa).getCpf().equals(txtCpf.getText().replaceAll("[\\.\\-]", ""))
            ) {
                aluno = (Aluno) pessoa;
            }
        }
        
        // Instanciando Matricula
        Matricula matricula = new Matricula(txtDataMatricula.getText().replaceAll("/", ""));
        matricula.setAluno(aluno);
        matricula.setTurma(turma);
        
        if (rdbAVista.isSelected()) {                     
            AVista aVista = new AVista();
            aVista.setValor(Double.valueOf(txtValor.getText()));
            aVista.setAgencia(Integer.valueOf(txtAgencia.getText()));
            aVista.setNCheque(Integer.valueOf(txtCheque.getText()));
            aVista.setPreData(txtDataPagto.getText().replaceAll("/", ""));
            
            matricula.setAVista(aVista);
            
            cadAVista.add(aVista);
        }
        else {
            APrazo aPrazo = new APrazo();
            aPrazo.setValor(Double.valueOf(txtValor.getText()));
            aPrazo.setDtVencimento(txtDataVencto.getText().replaceAll("/", ""));
            aPrazo.setTaxaJuros(Double.valueOf(txtJuros.getText()));
            aPrazo.setQtdeMensalidade(Integer.valueOf(txtMensalidades.getText()));
            
            matricula.setAPrazo(aPrazo);
            
            cadAPrazo.add(aPrazo);
        }
        
        // salvando matricula na lista
        cadMatricula.add(matricula);
        
        // Limpando campos
        txtDataMatricula.setText(null);
        cboCurso.setSelectedIndex(-1);
        txtValor.setText(null);
        cboTurma.setSelectedIndex(-1);
        txtCpf.setText(null);
        txtNome.setText(null);
        
        rdbAVista.setSelected(true);
        rdbParcelado.setSelected(false);
        txtAgencia.setText(null);
        txtCheque.setText(null);
        txtDataPagto.setText(null);
        
        txtMensalidades.setText(null);
        txtJuros.setText(null);
        txtDataVencto.setText(null);  
        
        // Habilitando botoes
        btnConsultar.setEnabled(true);
        btnInserir.setEnabled(false);
        btnAlterar.setEnabled(false);
        btnExcluir.setEnabled(false);
        
        // Habilitando campos
        txtDataMatricula.setEnabled(true);
        txtCpf.setEnabled(true);
        
        jPanel1.setEnabled(false);
        txtAgencia.setEnabled(false);
        txtCheque.setEnabled(false);
        txtDataPagto.setEnabled(false);
        
        txtMensalidades.setEnabled(false);
        txtJuros.setEnabled(false);
        txtDataVencto.setEnabled(false);
    }//GEN-LAST:event_btnInserirActionPerformed

