    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        
        // Validando CPF
        String cpf = txtCpf.getText().replaceAll("[\\.\\-]", "");
        
        if (!validarCPF(cpf)) {
            JOptionPane.showMessageDialog(null,"Informe um CPF válido", "Aviso", JOptionPane.ERROR_MESSAGE);
            txtCpf.requestFocus();
            return;
        }
        
        Aluno aluno = null;
        
        for (Pessoa pessoa : cadAlunoInstrutor) {
            if (
                    (pessoa instanceof Aluno) &&
                    (pessoa.getCpf().equals(cpf))
                ) {
                aluno = (Aluno) pessoa;
            }
        }
        
        // Cpf indicado não é de aluno
        if (aluno == null) {
            JOptionPane.showMessageDialog(null,"Informe um CPF de um aluno cadastrado", "Aviso", JOptionPane.ERROR_MESSAGE);
            txtCpf.requestFocus();
            return;
        }
                  
        int i;
        posMatricula = -1;  // padrao Matricula nao encontrada
        
        for (i = 0; i < cadMatricula.size(); i++) {
            if (cadMatricula.get(i).getAluno().getCpf().equals(cpf)) {
                posMatricula = i; 
            }
        }
        
        txtCpf.setEnabled(false); 
        jPanel1.setEnabled(true);
        txtNome.setText(aluno.getNome());
        btnConsultar.setEnabled(false);
        
        if (posMatricula > -1) {
            if (cadMatricula.get(posMatricula).getAVista() != null) {
                AVista aVista = cadMatricula.get(posMatricula).getAVista();
                txtAgencia.setText(String.valueOf(aVista.getAgencia()));
                txtCheque.setText(String.valueOf(aVista.getNCheque()));
                txtDataPagto.setText(aVista.getPreData());
                
                txtMensalidades.setText(null);
                txtJuros.setText(null);
                txtDataVencto.setText(null);
  
                rdbAVista.setSelected(true);
                txtAgencia.setEnabled(true);
                txtCheque.setEnabled(true);
                txtDataPagto.setEnabled(true);

                rdbParcelado.setSelected(false);
                txtMensalidades.setEnabled(false);
                txtJuros.setEnabled(false);
                txtDataVencto.setEnabled(false);
            }
            else {
                APrazo aPrazo = cadMatricula.get(posMatricula).getAPrazo();
                txtMensalidades.setText(String.valueOf(aPrazo.getQtdeMensalidade()));
                txtJuros.setText(String.valueOf(aPrazo.getTaxaJuros()));
                txtDataVencto.setText(aPrazo.getDtVencimento());
                
                txtAgencia.setText(null);
                txtCheque.setText(null);
                txtDataPagto.setText(null);
                
                rdbAVista.setSelected(false);
                txtAgencia.setEnabled(false);
                txtCheque.setEnabled(false);
                txtDataPagto.setEnabled(false);

                rdbParcelado.setSelected(true);
                txtMensalidades.setEnabled(true);
                txtJuros.setEnabled(true);
                txtDataVencto.setEnabled(true);
            } 
            
            btnInserir.setEnabled(false);
            btnAlterar.setEnabled(true);
            btnExcluir.setEnabled(true);    
            
            return;
        }
        
        txtAgencia.setText(null);
        txtCheque.setText(null);
        txtDataPagto.setText(null);
        
        txtMensalidades.setText(null);
        txtJuros.setText(null);
        txtDataVencto.setText(null);

        rdbAVista.setSelected(true);
        txtAgencia.setEnabled(true);
        txtCheque.setEnabled(true);
        txtDataPagto.setEnabled(true);

        rdbParcelado.setSelected(false);
        rdbParcelado.setEnabled(true);
        txtMensalidades.setEnabled(false);
        txtJuros.setEnabled(false);
        txtDataVencto.setEnabled(false);
        
        btnInserir.setEnabled(true);
        btnAlterar.setEnabled(false);
        btnExcluir.setEnabled(false);    
    }//GEN-LAST:event_btnConsultarActionPerformed

    private void cboCursoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboCursoActionPerformed
        int x;
        ArrayList<String> relTurmas;
        relTurmas = Turma.listarTurma(cadTurma);
        cboTurma.removeAllItems();
        
        for (x = 0; x < relTurmas.size(); x++){
            if(cadTurma.get(x).getCurso().getSigla().equals(cboCurso.getSelectedItem())){
                cboTurma.addItem(relTurmas.get(x));
                txtValor.setText(Double.toString(cadTurma.get(x).getCurso().getValor()));
            }            
        }
        cboTurma.setSelectedIndex(-1);
    }//GEN-LAST:event_cboCursoActionPerformed

